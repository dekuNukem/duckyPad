/**
  * @brief  Function implementing the kb_scan thread.
  * @param  argument: Not used 
  * @retval None
  */
/* USER CODE END Header_kb_scan_task */
void kb_scan_task(void const * argument)
{
  /* init code for FATFS */
  MX_FATFS_Init();

  /* init code for USB_DEVICE */
  MX_USB_DEVICE_Init();

  /* USER CODE BEGIN 5 */
  neopixel_off();
  mount_result = f_mount(&sd_fs, "", 1);
  HAL_GPIO_WritePin(OLED_RESET_GPIO_Port, OLED_RESET_Pin, GPIO_PIN_RESET);
  osDelay(10);
  HAL_GPIO_WritePin(OLED_RESET_GPIO_Port, OLED_RESET_Pin, GPIO_PIN_SET);
  osDelay(20);
  ssd1306_Init();
  ssd1306_Fill(Black);
	#ifdef FRANKENDUCK
  if (startup == 0)
	{
		startup = 1;
		ssd1306_Fill(Black);
		ssd1306_SetCursor(18,5);		
    ssd1306_WriteString("The FrankenDuck",Font_6x10,White);
		
    memset(temp_buf, 0, PATH_SIZE);
    sprintf(temp_buf, "FW V%d.%d.%d", fw_version_major_fd, fw_version_minor, fw_version_patch);
    ssd1306_SetCursor(35, 52);
    ssd1306_WriteString(temp_buf,Font_6x10,White);

    ssd1306_SetCursor(5, 25);
    ssd1306_WriteString("A DuckyPad Derivative",Font_6x10,White);
    ssd1306_UpdateScreen();
		for(int i = 0; i < 100; i++)
		{			
      HAL_IWDG_Refresh(&hiwdg);
			osDelay(30);
		}
	}
	startup = 1;
	#endif
  if(mount_result)
  {
    ssd1306_Fill(Black);
    ssd1306_SetCursor(0, 5);
    ssd1306_WriteString("Please Insert SD Card",Font_6x10,White);

    memset(temp_buf, 0, PATH_SIZE);
    sprintf(temp_buf, "FW V%d.%d.%d", fw_version_major, fw_version_minor, fw_version_patch);
    ssd1306_SetCursor(35, 52);
    ssd1306_WriteString(temp_buf,Font_6x10,White);

    ssd1306_SetCursor(0, 22);
    ssd1306_WriteString(instruction,Font_6x10,White);
    ssd1306_SetCursor(18, 35);
    ssd1306_WriteString(project_url,Font_6x10,White);
    ssd1306_UpdateScreen();
    while(1)
    {
      HAL_IWDG_Refresh(&hiwdg);
      osDelay(30);
    }
  }

  load_settings();
  scan_profiles();
  // this line must be after scan_profiles()
  uint8_t last_profile = get_last_profile();
  if(last_profile == 0)
    change_profile(NEXT_PROFILE);
  else
    restore_profile(last_profile);
  
  init_complete = 1;
  /* Infinite loop */
  for(;;)
  {
    HAL_IWDG_Refresh(&hiwdg);
    keyboard_update();
    osDelay(16);
  }
  /* USER CODE END 5 */ 
}

