/**
  * @brief  Function implementing the kb_scan thread.
  * @param  argument: Not used 
  * @retval None
  */
/* USER CODE END Header_kb_scan_task */
void kb_scan_task(void const * argument)
{
  /* init code for USB_DEVICE */
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 5 */
  neopixel_off();
  mount_result = f_mount(&sd_fs, "", 1);
  HAL_GPIO_WritePin(OLED_RESET_GPIO_Port, OLED_RESET_Pin, GPIO_PIN_RESET);
  HAL_Delay(10);
  HAL_GPIO_WritePin(OLED_RESET_GPIO_Port, OLED_RESET_Pin, GPIO_PIN_SET);
  HAL_Delay(20);
  ssd1306_Init();
  if(mount_result)
  {
    ssd1306_Fill(Black);
    ssd1306_SetCursor(0, 13);
    ssd1306_WriteString("Please Insert SD Card",Font_6x10,White);
    ssd1306_SetCursor(0, 37);
    ssd1306_WriteString(instruction,Font_6x10,White);
    ssd1306_SetCursor(18, 50);
    ssd1306_WriteString(project_url,Font_6x10,White);
    ssd1306_UpdateScreen();
    while(1)
    {
      HAL_IWDG_Refresh(&hiwdg);
      osDelay(30);
    }
  }

  scan_profiles();
  // this line must be after scan_profiles()
  uint8_t last_profile = get_last_profile();
  if(last_profile == 0)
    change_profile(NEXT_PROFILE);
  else
    restore_profile(last_profile);
   
  init_complete = 1;
  /* Infinite loop */
  for(;;)
  {
    HAL_IWDG_Refresh(&hiwdg);
    keyboard_update();
    osDelay(16);
  }
  /* USER CODE END 5 */ 
}

